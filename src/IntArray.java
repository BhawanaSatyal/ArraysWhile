//6. WAP to sum values of an int array


public class IntArray {

        public static void main(String[] args) { // main method

                int[] arr = {5, 10, 15, 20, 25}; // declared int array
                {
                int sum = 0; // initialise sum
                int i;
                for (i =0; i< arr.length; i++) // iterate through all elements to add them to sum
                sum += arr[i];

                System.out.println("The Sum of " +5+ " + " +10+ " + "  +15+ " + "  +20+ " + " +25+ " = " + sum +"\n\n\n\n"); // output


        }
      }
}
